#include <bits/stdc++.h>

using namespace std;

int main(){
    int b,n,r, d,c,v;
    vector<int> rv;
    while(scanf("%d %d", &b,&n)){
        for (size_t i = 0; i < b; i++){
            scanf("%d", &r);
            rv.push_back(r);
        }

        while(n--){
            scanf("%d %d %d",&d,&c,&v);
        }
    }
    
    

    return 0;
}